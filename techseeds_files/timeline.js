<a href="http://mitlaunchclubs.com" target="_blank">www.mitlaunchclubs.com</a><br><br>

                        <p>Through Launch Clubs, school entrepreneurship clubs are being started at schools around the world, with teams becoming entrepreneurs and starting real companies. The first year involved around 20 schools of Launch alumni with about 100 companies started. Finalists were invited to a mentorship and pitch competition at MIT in the spring of 2015. Launch Clubs has expanded to almost 100 schools this year.</p>
